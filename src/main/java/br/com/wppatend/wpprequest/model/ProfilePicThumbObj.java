package br.com.wppatend.wpprequest.model;

public class ProfilePicThumbObj {
	
	private String eurl;
	private String id;
	private String img;
	private String imgFull;
	private Object raw;
	private Long tag;
	public String getEurl() {
		return eurl;
	}
	public void setEurl(String eurl) {
		this.eurl = eurl;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getImgFull() {
		return imgFull;
	}
	public void setImgFull(String imgFull) {
		this.imgFull = imgFull;
	}
	public Object getRaw() {
		return raw;
	}
	public void setRaw(Object raw) {
		this.raw = raw;
	}
	public Long getTag() {
		return tag;
	}
	public void setTag(Long tag) {
		this.tag = tag;
	}

}
